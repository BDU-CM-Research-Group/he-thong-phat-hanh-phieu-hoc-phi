// ============================================
// DOM UTILITIES
// ============================================
const DOM = {
    get(id) {
        return document.getElementById(id);
    },

    show(id) {
        const el = this.get(id);
        if (el) el.style.display = 'inline-block';
    },

    hide(id) {
        const el = this.get(id);
        if (el) el.style.display = 'none';
    },

    setValue(id, value) {
        const el = this.get(id);
        if (el) el.value = value;
    },

    getValue(id) {
        return this.get(id)?.value || '';
    }
};

// ============================================
// UTILITIES
